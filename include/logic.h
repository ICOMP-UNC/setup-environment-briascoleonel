#pragma once

/**
 * @brief Add two numbers
 * 
 * @param first_number 
 * @param second_number 
 * @return first_number + second_number
 */
int add(int first_number, int second_number);

/**
 * @brief Substract two numbers
 * 
 * @param first_number 
 * @param second_number 
 * @return first_number - second_number
 */
int subtract(int first_number, int second_number);

/**
 * @brief Multiply two numbers
 * 
 * @param first_number 
 * @param second_number 
 * @return first_number * second number
 */
int multiply(int first_number, int second_number);

/**
 * @brief Divide two numbers
 * 
 * @param first_number 
 * @param second_number 
 * @return first_number / second_number iff second_number != 0
 */
double divide(int first_number, int second_number);

   